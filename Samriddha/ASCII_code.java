public class ASCII_code   
  {  
    public static void main(String[] args)   
    {  
      char ch1 = 'a';  
      char ch2 = 'b';  
      int asciivalue1 = ch1;  
      int asciivalue2 = ch2;  
      System.out.println("The ASCII value of " + ch1 + " is: " + asciivalue1);  
      System.out.println("The ASCII value of " + ch2 + " is: " + asciivalue2);  
    }  
  }
